// 分页获取案例
export function getMockCasesPaginated(page: number, size: number): {
  items: CaseItem[]
  total: number
  page: number
  size: number
  pages: number
} {
  const start = (page - 1) * size
  const end = start + size
  const items = mockCases.slice(start, end)

  return {
    items,
    total: mockCases.length,
    page,
    size,
    pages: Math.ceil(mockCases.length / size)
  }
}

